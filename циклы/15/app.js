// Дан массив числами, например: [10, 20, 30, 50, 235, 3000]. Выведите на экран только те числа из массива, которые начинаются на цифру 1, 2 или 5 -> 10, 20, 50, 235

const arr = [10, 20, 30, 50, 235, 3000],
    arrStr = [];
for (let i = 0; i < arr.length; i++) {
    arrStr.push(String(arr[i]));
}
for (let i of arrStr) {
    if (i[0] == '1' || i[0] == '2' || i[0] == '5') console.log(i);
}
